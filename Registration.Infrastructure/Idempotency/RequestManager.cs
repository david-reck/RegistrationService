using iPAS.Services.Registration.Domain.Exceptions;
using System;
using System.Threading.Tasks;

namespace iPAS.Services.Registration.Infrastructure.Idempotency
{
    public class RequestManager : IRequestManager
    {
        private readonly RegistrationContext _context;

        public RequestManager(RegistrationContext context)
        {
            _context = context ?? throw new ArgumentNullException(nameof(context));
        }


        public async Task<bool> ExistAsync(Guid id)
        {
            var request = await _context.
                FindAsync<ClientRequest>(id);

            return request != null;
        }

        public async Task CreateRequestForCommandAsync<T>(Guid id)
        {
            var exists = await ExistAsync(id);

            var request = exists ?
                throw new RegistrationDomainException($"Request with {id} already exists") :
                new ClientRequest()
                {
                    Id = id,
                    Name = typeof(T).Name,
                    Time = DateTime.UtcNow
                };

            _context.Add(request);

            await _context.SaveChangesAsync();
        }
    }
}
